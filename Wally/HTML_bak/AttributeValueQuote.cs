namespace Wally.HTML
{
    /// <summary>
    /// An Enum representing different types of Quotes used for surrounding attribute values
    /// </summary>
    internal enum AttributeValueQuote
    {
        /// <summary>
        /// A single quote mark '
        /// </summary>
        SingleQuote,

        /// <summary>
        /// A double quote mark "
        /// </summary>
        DoubleQuote
    }
}